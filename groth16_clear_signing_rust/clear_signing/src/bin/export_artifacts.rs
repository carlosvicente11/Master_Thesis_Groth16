// Exports everything the C verifier needs for the clear-signing E2E
// (plan step 5): ark-serialize artifacts + raw binary test vectors + generated
// C headers (VK constants and test vectors), using the same raw encodings the
// existing minimal_c_verifier decodes:
//   G1 = BE x||y (64 B), G2 = x.c0||x.c1||y.c0||y.c1 (128 B),
//   GT = 12 BE Fq coeffs in tower order (384 B), proof = A||B||C (256 B).

use std::fs;
use std::path::Path;

use ark_bn254::{Fq, Fq12, Fr, G1Affine, G2Affine};
use ark_ec::{AffineRepr, CurveGroup};
use ark_ff::{BigInteger, PrimeField};
use ark_groth16::prepare_verifying_key;
use std::ops::Neg;

use clear_signing_groth16::snark::{prove, setup, verify};
use clear_signing_groth16::template::{build_transfer_calldata, render_transfer_text};
use poseidon_preimage_groth16::params::poseidon_params;
use poseidon_preimage_groth16::serialize::to_bytes_compressed;

fn fq_be(e: &Fq) -> [u8; 32] {
    let le = e.into_bigint().to_bytes_le();
    let mut be = [0u8; 32];
    for (i, &b) in le.iter().enumerate() {
        be[31 - i] = b;
    }
    be
}

fn fr_be(e: &Fr) -> [u8; 32] {
    let le = e.into_bigint().to_bytes_le();
    let mut be = [0u8; 32];
    for (i, &b) in le.iter().enumerate() {
        be[31 - i] = b;
    }
    be
}

fn g1_raw(p: &G1Affine) -> Vec<u8> {
    [fq_be(&p.x).as_slice(), &fq_be(&p.y)].concat()
}

fn g2_raw(p: &G2Affine) -> Vec<u8> {
    [
        fq_be(&p.x.c0).as_slice(),
        &fq_be(&p.x.c1),
        &fq_be(&p.y.c0),
        &fq_be(&p.y.c1),
    ]
    .concat()
}

fn gt_raw(f: &Fq12) -> Vec<u8> {
    [
        &f.c0.c0.c0, &f.c0.c0.c1, &f.c0.c1.c0, &f.c0.c1.c1, &f.c0.c2.c0, &f.c0.c2.c1,
        &f.c1.c0.c0, &f.c1.c0.c1, &f.c1.c1.c0, &f.c1.c1.c1, &f.c1.c2.c0, &f.c1.c2.c1,
    ]
    .iter()
    .flat_map(|c| fq_be(c))
    .collect()
}

fn c_array(out: &mut String, name: &str, bytes: &[u8]) {
    out.push_str(&format!(
        "static const uint8_t {name}[{}] = {{\n",
        bytes.len()
    ));
    for chunk in bytes.chunks(16) {
        out.push_str("    ");
        for b in chunk {
            out.push_str(&format!("0x{b:02x}, "));
        }
        out.push('\n');
    }
    out.truncate(out.trim_end_matches(['\n', ' ', ',']).len());
    out.push_str("\n};\n\n");
}

fn main() {
    let params = poseidon_params();
    let dir = Path::new("artifacts");
    fs::create_dir_all(dir).expect("create artifacts dir");

    // Sample transfer: 12.5 USDC, address exercising all hex chars.
    let to: [u8; 20] = [
        0xde, 0xad, 0xbe, 0xef, 0x01, 0x23, 0x45, 0x67, 0x89, 0xab, 0xcd, 0xef, 0x11, 0x22, 0x33,
        0x44, 0x55, 0x66, 0x77, 0x88,
    ];
    let calldata = build_transfer_calldata(&to, 12_500_000);
    let text = render_transfer_text(&calldata).expect("render");

    println!("Setup...");
    let artifacts = setup(&params).expect("setup");
    println!("Prove...");
    let (proof, public_inputs) = prove(&artifacts.pk, &params, &calldata, &text).expect("prove");
    assert!(verify(&artifacts.pvk, &public_inputs, &proof), "self-check");
    println!("Rust verify: PASS");

    // --- ark-serialize artifacts ---
    for (name, bytes) in [
        ("clear_signing_transfer_pk.bin", to_bytes_compressed(&artifacts.pk)),
        ("clear_signing_transfer_vk.bin", to_bytes_compressed(&artifacts.vk)),
        ("clear_signing_transfer_proof.bin", to_bytes_compressed(&proof)),
        (
            "clear_signing_transfer_public_inputs.bin",
            to_bytes_compressed(&public_inputs),
        ),
    ] {
        fs::write(dir.join(name), &bytes).expect("write");
        println!("{name}: {} bytes", bytes.len());
    }

    // --- raw vectors for the C side ---
    let proof_raw = [g1_raw(&proof.a), g2_raw(&proof.b), g1_raw(&proof.c)].concat();
    assert_eq!(proof_raw.len(), 256);
    let publics_raw = [fr_be(&public_inputs[0]).as_slice(), &fr_be(&public_inputs[1])].concat();
    fs::write(dir.join("proof_raw.bin"), &proof_raw).expect("write");
    fs::write(dir.join("calldata.bin"), &calldata).expect("write");
    fs::write(dir.join("text.bin"), &text).expect("write");
    fs::write(dir.join("public_inputs_raw.bin"), &publics_raw).expect("write");

    // --- generated C headers ---
    let vk = &artifacts.vk;
    let pvk = prepare_verifying_key(vk);
    assert_eq!(vk.gamma_abc_g1.len(), 3, "expect 2 public inputs");
    let neg_gamma = vk.gamma_g2.into_group().neg().into_affine();
    let neg_delta = vk.delta_g2.into_group().neg().into_affine();

    let mut h = String::new();
    h.push_str(
        "// Auto-generated by clear_signing export_artifacts. Do not edit.\n\
         //\n\
         // Groth16 BN254 verifying key for the two-hash clear-signing circuit\n\
         // (v1 ERC-20 transfer). Public inputs: [h_c, h_t].\n\
         //\n\
         // IC = 3 x G1 (64 B each), e(alpha,beta) = GT (384 B),\n\
         // -gamma/-delta = G2 (128 B each).\n\n\
         #ifndef VK_CLEAR_SIGNING_H\n\
         #define VK_CLEAR_SIGNING_H\n\n\
         #include <stdint.h>\n\n\
         #define CLEAR_SIGNING_NUM_PUBLIC_INPUTS 2\n\n",
    );
    let ic_flat: Vec<u8> = vk.gamma_abc_g1.iter().flat_map(|p| g1_raw(p)).collect();
    c_array(&mut h, "CS_VK_IC_G1", &ic_flat); // IC[0] || IC[1] || IC[2]
    c_array(&mut h, "CS_VK_ALPHA_BETA_GT", &gt_raw(&pvk.alpha_g1_beta_g2));
    c_array(&mut h, "CS_VK_NEG_GAMMA_G2", &g2_raw(&neg_gamma));
    c_array(&mut h, "CS_VK_NEG_DELTA_G2", &g2_raw(&neg_delta));
    h.push_str("#endif // VK_CLEAR_SIGNING_H\n");
    fs::write(dir.join("vk_clear_signing.h"), &h).expect("write vk header");

    let mut v = String::new();
    v.push_str(
        "// Auto-generated by clear_signing export_artifacts. Do not edit.\n\
         //\n\
         // E2E test vectors: calldata C, canonical text T, Groth16 proof, and\n\
         // the expected public inputs [h_c, h_t] (BE) the chip must recompute.\n\n\
         #ifndef CLEAR_SIGNING_VECTORS_H\n\
         #define CLEAR_SIGNING_VECTORS_H\n\n\
         #include <stdint.h>\n\n",
    );
    c_array(&mut v, "CS_CALLDATA", &calldata);
    c_array(&mut v, "CS_TEXT", &text);
    c_array(&mut v, "CS_PROOF", &proof_raw);
    c_array(&mut v, "CS_EXPECTED_H_C", &fr_be(&public_inputs[0]));
    c_array(&mut v, "CS_EXPECTED_H_T", &fr_be(&public_inputs[1]));
    v.push_str("#endif // CLEAR_SIGNING_VECTORS_H\n");
    fs::write(dir.join("clear_signing_vectors.h"), &v).expect("write vectors header");

    println!("Wrote raw vectors + vk_clear_signing.h + clear_signing_vectors.h to artifacts/");
    println!("h_c = {}", hex(&fr_be(&public_inputs[0])));
    println!("h_t = {}", hex(&fr_be(&public_inputs[1])));
}

fn hex(bytes: &[u8]) -> String {
    bytes.iter().map(|b| format!("{b:02x}")).collect()
}
